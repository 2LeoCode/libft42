#include <stddef.h>

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n && s1[i] && s2[i] && s1[i] == s2[i])
		++i;
	return ((i != n) * ((unsigned char)s1[i] - (unsigned char)s2[i]));
}
